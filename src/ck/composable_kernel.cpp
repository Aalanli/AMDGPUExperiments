#include "ck/tensor_operation/gpu/device/device_gemm_streamk.hpp"


int main(int argc, char* argv[]) { 
    int M = 1024;
    int N = 1024;
    int K = 1024;
    int StrideA = 1024;
    int StrideB = 1024;
    int StrideC = 1024;
    AElementOp a_element_op{};
    BElementOp b_element_op{};
    CElementOp c_element_op{};
    float *A;
    float *B;
    float *C;
    hipMalloc(&A, M*K*sizeof(float));
    hipMalloc(&B, K*N*sizeof(float));
    hipMalloc(&C, M*N*sizeof(float));

    auto gemm      = DeviceGemmInstance{};
    auto invoker   = gemm.MakeInvoker();
    float ave_time = 0;

    auto argument = gemm.MakeArgument(
        A, B, C,
        M,
        N,
        K,
        StrideA,
        StrideB,
        StrideC,
        a_element_op,
        b_element_op,
        c_element_op);

    if(!gemm.IsSupportedArgument(argument))
    {
        std::cerr << gemm.GetTypeString() << " does not support this problem" << std::endl;
    }

    ave_time = invoker.Run(argument, StreamConfig{nullptr, true});
    hipFree(A);
    hipFree(B);
    hipFree(C);    
}
